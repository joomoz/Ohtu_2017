package ohtu;

public class Sovelluslogiikka {
 
    private int tulos, edellinen;
 
    public void setTulos(int luku) {
        tulos = luku;
    }
 
    public int getTulos() {
        return tulos;
    }
    
    public void setEdellinen(int luku) {
        edellinen = luku;
    }
    
    public int getEdellinen() {
        return edellinen;
    }
    
}